    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int dongDangChon = tbTTNguoiThue.getSelectedRow();
        if (dongDangChon == 0) {
            ThongBao.ThongBaoDonGian("Thông Báo", "Không thể xóa người đại diện!");
        }
        if (dongDangChon < 0) {
            ThongBao.ThongBaoDonGian("Thông Báo", "Bạn chưa chọn đối tượng cần xóa");
        } else {
            int XacNhan = JOptionPane.showConfirmDialog(null, "Bạn có chắc xóa không?", "Thông báo xác nhận", JOptionPane.OK_CANCEL_OPTION);
            if (XacNhan == JOptionPane.CANCEL_OPTION) {
                return;
            }
        }
        //Lấy danh sách các sp  cần xóa
        int dongCanXoa[] = tbTTNguoiThue.getSelectedRows();

        for (int i = 0; i < dongCanXoa.length; i++) {
            ArrayList<DaiDienThue> ID = BLL.BLLDaiDienThue.FindByMaNDD(txtMaNguoiDaiDien.getText());
            String ten = tbTTNguoiThue.getValueAt(dongCanXoa[i], 0).toString();
            BLL.BLLThanNhanThue.Delete(ID.get(0).getMaNguoiDaiDien(), ten);
        }
        loadTTPT();
    }//GEN-LAST:event_btnXoaActionPerformed

    public void loadTTPT() {
        try {
            int indexRow = tbTTPhongThue.getSelectedRow();
        if (!tbTTPhongThue.getValueAt(indexRow, 2).toString().equals("Trống")) {
//            ThongBao.ThongBaoDonGian("Thông báo", "lo");
            ArrayList<DaiDienThue> arr = BLL.BLLDaiDienThue.FindDDThue(tbTTPhongThue.getValueAt(indexRow, 0).toString());
            ArrayList<ThanNhanThue> arrTN = BLL.BLLThanNhanThue.FindByMaNDD(tbTTPhongThue.getValueAt(indexRow, 3).toString());
            BLL.BLLDaiDienThue.doDataTableTTThue(arr, tbTTNguoiThue);
            BLL.BLLThanNhanThue.doDataTableTTThue(arrTN, tbTTNguoiThue);
            txtMaNDDtb.setText(arr.get(0).getMaNguoiDaiDien());
            txtTenPhongtb.setText(tbTTPhongThue.getValueAt(indexRow, 1).toString());
        } else {
            DefaultTableModel tbModel = (DefaultTableModel) tbTTNguoiThue.getModel();
            tbModel.setRowCount(0);
            txtMaNDDtb.setText("");
            txtTenPhongtb.setText("");
        }
        } catch (Exception e) {
        }
        
    }
