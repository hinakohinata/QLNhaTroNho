    public boolean isThue() {
        ArrayList<Phong> arrPT = BLL.BLLPhong.FindByTingTrang();
        for (Phong i : arrPT) {
            if (cbPhong.getSelectedItem().toString().equals(i.getTenPhong())) {
                ThongBao.ThongBaoDonGian("Thông báo", "Phòng đã có người thuê!");
                return false;
            } else {
                ThongBao.ThongBaoDonGian("Thông báo", "ok");
            }
        }
        return true;
    }

    public String reMaPhong(String tenPhong) {
        ArrayList<Phong> arrPT = BLL.BLLPhong.GetAll();
        for (Phong i : arrPT) {
            if (tenPhong.equals(i.getTenPhong())) {
                return i.getMaPhong();
            }
        }
        return null;
    }

