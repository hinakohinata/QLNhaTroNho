    private void tbTTNguoiThueMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbTTNguoiThueMouseClicked
        // TODO add your handling code here:
        int indexRow = tbTTNguoiThue.getSelectedRow();
        txtMaNguoiDaiDien.setText(txtMaNDDtb.getText());
        BLL.BLLPhong.HienThiPhongCBB(cbPhong, txtTenPhongtb.getText());
        System.out.println(txtTenPhongtb.getText());
        if (indexRow == 0) {
            rdVaiTro.setSelected(true);
        } else {
            rdVaiTro.setSelected(false);
        }
        txtTen.setText(tbTTNguoiThue.getValueAt(indexRow, 0).toString());
        if (tbTTNguoiThue.getValueAt(indexRow, 1) == null) {
            txtCMND.setText("");
        } else {
            txtCMND.setText(tbTTNguoiThue.getValueAt(indexRow, 1).toString());
        }
        dateNgaySinh.setDate(ChuyenDoi.LayNgayDate(tbTTNguoiThue.getValueAt(indexRow, 2).toString()));
        txtQueQuan.setText(tbTTNguoiThue.getValueAt(indexRow, 3).toString());
        dateNgayBDThue.setDate(ChuyenDoi.LayNgayDate(tbTTNguoiThue.getValueAt(0, 4).toString()));
    }//GEN-LAST:event_tbTTNguoiThueMouseClicked

